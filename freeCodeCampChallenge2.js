/**
 Today's challenge is inspired by the release of ChatGPT on November 30, 2022.

Given a string of one or more sentences, determine if it was likely generated by AI using the following rules:

It contains two or more dashes (-).

It contains two or more sets of parenthesis (()). Text can be within the parenthesis.

It contains three or more words with 7 or more letters.

Words are separated by a single space and only consist of letters (A-Z). Don't include punctuation or other non-letters as part of a word.

If the given sentence meets any of the rules above, return "AI", otherwise, return "Human".

1. detectAI("The quick brown fox jumped over the lazy dog.") should return "Human".
Waiting:2. detectAI("The hypersonic brown fox - jumped (over) the lazy dog.") should return "Human".
Waiting:3. detectAI("Yes - you're right! I made a mistake there - let me try again.") should return "AI".
Waiting:4. detectAI("The extraordinary students were studying vivaciously.") should return "AI".
Waiting:5. detectAI("The (excited) student was (coding) in the library.") should return "AI".


 */

function detectiveAI (text) {
  let textLen = text.split(' ') //this splits the sentence in to words

  if(text.includes('((') || text.includes('))') || text.includes('--')){text = "AI"}
  textLen.forEach(word => {
    if(word.length >= 7) { text = 'AI'}
    else{ text = 'Human'}
        });
  return text;
}

console.log( detectiveAI("The quick brown fox jumped over the lazy dog.")) //should return "Human".
console.log( detectiveAI("The hypersonic brown fox - jumped (over) the lazy dog.")) //should return "Human".
console.log( detectiveAI("Yes - you're right! I made a mistake there - let me try again.")) //should return "AI".
console.log( detectiveAI("The extraordinary students were studying vivaciously.")) //should return "AI".
console.log( detectiveAI("The (excited) student was (coding) in the library.")) //should return "AI".
